def build_markdown(path: str) -> None:
    lines: list[str] = []
    for style, text in DOC:
        if style == "title":
            lines.append(f"# {text}\n")
        elif style == "sub":
            lines.append(f"## {text}\n")
        elif style == "author":
            lines.append(f"*{text}*\n")
        elif style == "h1":
            lines.append(f"## {text}\n")
        elif style == "h2":
            lines.append(f"### {text}\n")
        elif style == "warn":
            lines.append(f"> ⚠ {text}\n")
        elif style == "code":
            lines.append("```\n" + text + "\n```\n")
        else:
            lines.append(text + "\n")
    with open(path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))


def build_docx(path: str) -> None:
    from docx import Document
    from docx.shared import Pt, RGBColor

    doc = Document()
    for style, text in DOC:
        if style == "title":
            doc.add_heading(text, level=0)
        elif style == "sub":
            p = doc.add_paragraph()
            r = p.add_run(text)
            r.italic = True
            r.font.size = Pt(13)
        elif style == "author":
            p = doc.add_paragraph()
            r = p.add_run(text)
            r.font.size = Pt(10)
            r.font.color.rgb = RGBColor(0x55, 0x55, 0x55)
        elif style == "h1":
            doc.add_heading(text, level=1)
        elif style == "h2":
            doc.add_heading(text, level=2)
        elif style == "warn":
            p = doc.add_paragraph()
            r = p.add_run("⚠ " + text)
            r.bold = True
        elif style == "code":
            p = doc.add_paragraph()
            r = p.add_run(text)
            r.font.name = "Consolas"
            r.font.size = Pt(9)
        else:
            doc.add_paragraph(text)
    doc.save(path)


def main() -> None:
    md = os.path.join(ROOT, "MANUSCRIPT.ru.md")
    dx = os.path.join(ROOT, "spectral_primes.docx")
    build_markdown(md)
    build_docx(dx)
    print(f"wrote {md}")
    print(f"wrote {dx}")


